    let index = 0;
    const total = data.length;
    let auto = false;
    let voiceOn = false;
    let autoTimer;

    function showQuestion() {
      const q = document.getElementById("question");
      const a = document.getElementById("answer");
      const c = document.getElementById("counter");
      const p = document.getElementById("progress");
      const s = document.getElementById("status");

      q.textContent = "प्रश्न " + (index + 1) + ": " + data[index].q;
      a.textContent = "उत्तर: " + data[index].a;
      c.textContent = (index + 1) + " / " + total;
      p.style.width = ((index + 1) / total * 100) + "%";

      if (voiceOn) {
        speakQuestion(data[index].q + ". " + data[index].a);
      }

      s.textContent = auto ? "Auto Mode ON" : "";
    }

    function nextQuestion() {
      index = (index + 1) % total;
      showQuestion();
    }

    function prevQuestion() {
      index = (index - 1 + total) % total;
      showQuestion();
    }

    function toggleAuto() {
      auto = !auto;
      const s = document.getElementById("status");
      s.textContent = auto ? "Auto Mode ON" : "Auto Mode OFF";
      if (auto) {
        autoTimer = setInterval(nextQuestion, 5000);
      } else {
        clearInterval(autoTimer);
      }
    }

    function toggleVoice() {
      voiceOn = !voiceOn;
      const s = document.getElementById("status");
      s.textContent = voiceOn ? "🔊 Voice ON" : "🔇 Voice OFF";
      if (voiceOn) {
        speakQuestion(data[index].q + ". " + data[index].a);
      } else {
        window.speechSynthesis.cancel();
      }
    }

    function speakQuestion(text) {
      window.speechSynthesis.cancel();
      const utterance = new SpeechSynthesisUtterance(text);
      utterance.lang = "hi-IN";
      utterance.rate = 1;
      window.speechSynthesis.speak(utterance);
    }

